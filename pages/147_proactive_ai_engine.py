def _set_profile_key(uid: int, key: str, value: str, confidence: float = 0.8):
    conn = get_conn()
    try:
        if USE_POSTGRES:
            execute(conn, f"""
                INSERT INTO intent_profile (user_id, key, value, confidence, updated_at)
                VALUES ({PH},{PH},{PH},{PH},{PH})
                ON CONFLICT (user_id, key) DO UPDATE
                SET value={PH}, confidence={PH}, updated_at={PH}
            """, (uid, key, value, confidence, datetime.now().isoformat(),
                  value, confidence, datetime.now().isoformat()))
        else:
            execute(conn, f"""
                INSERT OR REPLACE INTO intent_profile (user_id, key, value, confidence, updated_at)
                VALUES ({PH},{PH},{PH},{PH},{PH})
            """, (uid, key, value, confidence, datetime.now().isoformat()))
        conn.commit()
    except Exception:
        pass
    finally:
        conn.close()


def _save_insight(uid: int, insight_type: str, title: str, body: str,
                  priority: str = "normal", source_signals: str = "") -> int:
    conn = get_conn()
