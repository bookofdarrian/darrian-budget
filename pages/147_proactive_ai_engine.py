def _ensure_tables():
    """Create all tables for the Proactive AI engine."""
    conn = get_conn()

    # Behavioral signals captured from every source in the app
    execute(conn, f"""
        CREATE TABLE IF NOT EXISTS intent_signals (
            id          INTEGER PRIMARY KEY {'SERIAL' if USE_POSTGRES else 'AUTOINCREMENT'},
            user_id     INTEGER DEFAULT 0,
            signal_type TEXT NOT NULL,
            source      TEXT NOT NULL,
            label       TEXT,
            value       REAL DEFAULT 0,
            content     TEXT,
            weight      REAL DEFAULT 1.0,
            captured_at TEXT DEFAULT (datetime('now'))
        )
    """.replace("INTEGER PRIMARY KEY SERIAL", "SERIAL PRIMARY KEY"))

    # Proactive insights generated by the AI
    execute(conn, f"""
        CREATE TABLE IF NOT EXISTS proactive_insights (
            id              INTEGER PRIMARY KEY {'SERIAL' if USE_POSTGRES else 'AUTOINCREMENT'},
            user_id         INTEGER DEFAULT 0,
            insight_type    TEXT,
            title           TEXT,
            body            TEXT,
            priority        TEXT DEFAULT 'normal',
            source_signals  TEXT,
            feedback        TEXT,
            dismissed       INTEGER DEFAULT 0,
            created_at      TEXT DEFAULT (datetime('now'))
        )
    """.replace("INTEGER PRIMARY KEY SERIAL", "SERIAL PRIMARY KEY"))

    # Manual context notes the user injects into the AI's reasoning window
    execute(conn, f"""
        CREATE TABLE IF NOT EXISTS intent_context_notes (
            id          INTEGER PRIMARY KEY {'SERIAL' if USE_POSTGRES else 'AUTOINCREMENT'},
            user_id     INTEGER DEFAULT 0,
            content     TEXT NOT NULL,
            pinned      INTEGER DEFAULT 0,
            created_at  TEXT DEFAULT (datetime('now'))
        )
    """.replace("INTEGER PRIMARY KEY SERIAL", "SERIAL PRIMARY KEY"))

    # User's personalization profile — AI-extracted goals, values, patterns
    execute(conn, f"""
        CREATE TABLE IF NOT EXISTS intent_profile (
            id          INTEGER PRIMARY KEY {'SERIAL' if USE_POSTGRES else 'AUTOINCREMENT'},
            user_id     INTEGER DEFAULT 0,
            key         TEXT NOT NULL,
            value       TEXT,
            confidence  REAL DEFAULT 0.7,
            updated_at  TEXT DEFAULT (datetime('now')),
            UNIQUE(user_id, key)
        )
    """.replace("INTEGER PRIMARY KEY SERIAL", "SERIAL PRIMARY KEY").replace(
        "UNIQUE(user_id, key)", "UNIQUE(user_id, key)"
    ))

    conn.commit()
    conn.close()


_ensure_tables()

