def _collect_finance_signals(uid: int) -> list[dict]:
    """Pull spending patterns and budget signals."""
    signals = []
    conn = get_conn()
    try:
        today = date.today()
        this_month = today.strftime("%Y-%m")
        last_month = (today.replace(day=1) - timedelta(days=1)).strftime("%Y-%m")

        # This month's spending by category
        rows = execute(conn, f"""
            SELECT category, SUM(actual) as total
            FROM expenses
            WHERE month = {PH}
            GROUP BY category
            ORDER BY total DESC
        """, (this_month,)).fetchall()

        for r in rows:
            cat, total = r[0], r[1] or 0
            if total > 0:
                signals.append({
                    "signal_type": "finance",
                    "source": "budget",
                    "label": f"Spent ${total:,.0f} in {cat} this month",
                    "value": total,
                    "content": json.dumps({"category": cat, "amount": total, "month": this_month}),
                    "weight": min(2.0, total / 200),
                })

        # Income vs spend gap
        income_row = execute(conn, f"""
            SELECT SUM(amount) FROM income WHERE month = {PH}
        """, (this_month,)).fetchone()
        income = (income_row[0] or 0) if income_row else 0

        total_spend = sum(s["value"] for s in signals if s["signal_type"] == "finance")
        if income > 0:
            gap = income - total_spend
            signals.append({
                "signal_type": "finance",
                "source": "budget",
                "label": f"Remaining budget: ${gap:,.0f} of ${income:,.0f} income",
                "value": gap,
                "content": json.dumps({"income": income, "spent": total_spend, "remaining": gap, "month": this_month}),
                "weight": 1.5,
            })

        # Month-over-month spend change
        last_rows = execute(conn, f"""
            SELECT SUM(actual) FROM expenses WHERE month = {PH}
        """, (last_month,)).fetchone()
        last_total = (last_rows[0] or 0) if last_rows else 0

        if last_total > 0 and total_spend > 0:
            pct = ((total_spend - last_total) / last_total) * 100
            direction = "up" if pct > 0 else "down"
            signals.append({
                "signal_type": "pattern",
                "source": "budget",
                "label": f"Spending {direction} {abs(pct):.0f}% vs last month",
                "value": pct,
                "content": json.dumps({"this_month": total_spend, "last_month": last_total, "change_pct": pct}),
                "weight": 1.8 if abs(pct) > 15 else 0.8,
            })

        # Expenses with no projected amount (unplanned)
        unplanned = execute(conn, f"""
            SELECT COUNT(*) FROM expenses
            WHERE month = {PH} AND (projected IS NULL OR projected = 0) AND actual > 0
        """, (this_month,)).fetchone()
        if unplanned and unplanned[0] > 0:
            signals.append({
                "signal_type": "finance",
                "source": "budget",
                "label": f"{unplanned[0]} unplanned expense(s) logged this month",
                "value": unplanned[0],
                "content": json.dumps({"count": unplanned[0]}),
                "weight": 1.2,
            })

    except Exception:
        pass
    finally:
        conn.close()
    return signals


def _collect_todo_signals(uid: int) -> list[dict]:
    """Pull task patterns and urgency signals."""
    signals = []
    conn = get_conn()
    try:
        today_str = date.today().isoformat()
        tomorrow_str = (date.today() + timedelta(days=1)).isoformat()
        week_str = (date.today() + timedelta(days=7)).isoformat()

        # Overdue tasks
        overdue = execute(conn, f"""
            SELECT id, title, due_date, priority FROM pa_tasks
            WHERE status = 'open' AND due_date IS NOT NULL AND due_date < {PH}
            ORDER BY due_date ASC
        """, (today_str,)).fetchall()

        for row in overdue:
            signals.append({
                "signal_type": "todo",
                "source": "tasks",
                "label": f"OVERDUE: {row[1][:60]} (due {row[2]})",
                "value": -1,
                "content": json.dumps({"id": row[0], "title": row[1], "due": row[2], "priority": row[3]}),
                "weight": 3.0 if row[3] == "high" else 2.0,
            })

        # Due today or tomorrow
        due_soon = execute(conn, f"""
            SELECT id, title, due_date, priority FROM pa_tasks
            WHERE status = 'open' AND due_date BETWEEN {PH} AND {PH}
            ORDER BY due_date ASC
        """, (today_str, tomorrow_str)).fetchall()

        for row in due_soon:
            signals.append({
                "signal_type": "todo",
                "source": "tasks",
                "label": f"Due soon: {row[1][:60]} ({row[2]})",
                "value": 0,
                "content": json.dumps({"id": row[0], "title": row[1], "due": row[2], "priority": row[3]}),
                "weight": 2.5 if row[3] == "high" else 1.5,
            })

        # High priority open tasks
        high_prio = execute(conn, f"""
            SELECT COUNT(*) FROM pa_tasks
            WHERE status = 'open' AND priority = 'high'
        """).fetchone()
        if high_prio and high_prio[0] > 0:
            signals.append({
                "signal_type": "todo",
                "source": "tasks",
                "label": f"{high_prio[0]} high-priority task(s) open",
                "value": high_prio[0],
                "content": json.dumps({"count": high_prio[0]}),
                "weight": 1.8,
            })

        # Completion rate (last 30 days)
        done_30 = execute(conn, f"""
            SELECT COUNT(*) FROM pa_tasks
            WHERE status = 'done'
            AND completed_at >= datetime('now', '-30 days')
        """).fetchone()
        created_30 = execute(conn, f"""
            SELECT COUNT(*) FROM pa_tasks
            WHERE created_at >= datetime('now', '-30 days')
        """).fetchone()
        done_n = done_30[0] if done_30 else 0
        created_n = created_30[0] if created_30 else 0
        if created_n > 0:
            rate = (done_n / created_n) * 100
            signals.append({
                "signal_type": "pattern",
                "source": "tasks",
                "label": f"Task completion rate: {rate:.0f}% over last 30 days",
                "value": rate,
                "content": json.dumps({"done": done_n, "created": created_n, "rate": rate}),
                "weight": 0.8,
            })

        # Total open task count
        open_count = execute(conn, f"""
            SELECT COUNT(*) FROM pa_tasks WHERE status = 'open'
        """).fetchone()
        if open_count and open_count[0] > 0:
            signals.append({
                "signal_type": "todo",
                "source": "tasks",
                "label": f"{open_count[0]} total open tasks",
                "value": open_count[0],
                "content": json.dumps({"count": open_count[0]}),
                "weight": 0.5 + min(1.5, open_count[0] / 10),
            })

    except Exception:
        pass
    finally:
        conn.close()
    return signals


def _collect_notes_signals(uid: int) -> list[dict]:
    """Pull notes activity and topic signals."""
    signals = []
    conn = get_conn()
    try:
        # Recent notes (last 7 days)
        recent = execute(conn, f"""
            SELECT title, category, tags, updated_at FROM notes
            WHERE updated_at >= datetime('now', '-7 days')
            ORDER BY updated_at DESC
            LIMIT 10
        """).fetchall()

        if recent:
            topics = [r[0] for r in recent if r[0]]
            signals.append({
                "signal_type": "notes",
                "source": "notes",
                "label": f"Active in Notes: {', '.join(topics[:4])}",
                "value": len(recent),
                "content": json.dumps({"recent_notes": [r[0] for r in recent], "count": len(recent)}),
                "weight": 0.7,
            })

        # Notes with no category (potential clutter)
        uncategorized = execute(conn, f"""
            SELECT COUNT(*) FROM notes
            WHERE (category IS NULL OR category = '') AND archived = 0
        """).fetchone()
        if uncategorized and uncategorized[0] > 3:
            signals.append({
                "signal_type": "notes",
                "source": "notes",
                "label": f"{uncategorized[0]} uncategorized notes (possible clutter)",
                "value": uncategorized[0],
                "content": json.dumps({"count": uncategorized[0]}),
                "weight": 0.5,
            })

        # Total notes for context
        total_notes = execute(conn, "SELECT COUNT(*) FROM notes WHERE archived = 0").fetchone()
        if total_notes and total_notes[0] > 0:
            signals.append({
                "signal_type": "notes",
                "source": "notes",
                "label": f"Notes library: {total_notes[0]} active notes",
                "value": total_notes[0],
                "content": json.dumps({"total": total_notes[0]}),
                "weight": 0.3,
            })

    except Exception:
        pass
    finally:
        conn.close()
    return signals


def _collect_creator_signals(uid: int) -> list[dict]:
    """Pull creator content pipeline signals."""
    signals = []
    conn = get_conn()
    try:
        today_str = date.today().isoformat()
        week_str = (date.today() + timedelta(days=7)).isoformat()

        # Content due this week
        due_content = execute(conn, f"""
            SELECT title, channel_id, status, publish_date FROM cc_content
            WHERE status != 'published'
            AND publish_date IS NOT NULL
            AND publish_date <= {PH}
            ORDER BY publish_date ASC
            LIMIT 10
        """, (week_str,)).fetchall()

        for row in due_content:
            overdue = row[3] and row[3] < today_str
            label = f"{'OVERDUE' if overdue else 'Due'} content: {row[0][:50]} ({row[3]})"
            signals.append({
                "signal_type": "creator",
                "source": "creator_companion",
                "label": label,
                "value": -1 if overdue else 0,
                "content": json.dumps({"title": row[0], "status": row[2], "due": row[3]}),
                "weight": 2.5 if overdue else 1.5,
            })

        # Content in draft (stalled)
        drafts = execute(conn, f"""
            SELECT COUNT(*) FROM cc_content WHERE status = 'draft'
        """).fetchone()
        if drafts and drafts[0] > 0:
            signals.append({
                "signal_type": "creator",
                "source": "creator_companion",
                "label": f"{drafts[0]} content draft(s) stalled / not published",
                "value": drafts[0],
                "content": json.dumps({"draft_count": drafts[0]}),
                "weight": 1.0,
            })

        # Idea backlog
        ideas = execute(conn, f"""
            SELECT COUNT(*) FROM cc_ideas WHERE status = 'new'
        """).fetchone()
        if ideas and ideas[0] > 0:
            signals.append({
                "signal_type": "creator",
                "source": "creator_companion",
                "label": f"{ideas[0]} content idea(s) in backlog",
                "value": ideas[0],
                "content": json.dumps({"idea_count": ideas[0]}),
                "weight": 0.6,
            })

    except Exception:
        pass
    finally:
        conn.close()
    return signals


def _collect_soleops_signals(uid: int) -> list[dict]:
    """Pull SoleOps inventory and sales signals."""
    signals = []
    conn = get_conn()
    try:
        # Stale inventory (listed > 60 days, unsold)
        stale = execute(conn, f"""
            SELECT COUNT(*), SUM(buy_price) FROM soleops_inventory
            WHERE status = 'listed'
            AND listed_date <= date('now', '-60 days')
        """).fetchone()
        if stale and stale[0] and stale[0] > 0:
            tied_up = stale[1] or 0
            signals.append({
                "signal_type": "soleops",
                "source": "soleops_inventory",
                "label": f"{stale[0]} item(s) listed 60+ days — ${tied_up:,.0f} capital tied up",
                "value": tied_up,
                "content": json.dumps({"stale_count": stale[0], "capital": tied_up}),
                "weight": 2.0,
            })

        # Total active inventory value
        inventory = execute(conn, f"""
            SELECT COUNT(*), SUM(buy_price) FROM soleops_inventory
            WHERE status IN ('listed', 'in_stock')
        """).fetchone()
        if inventory and inventory[0] and inventory[0] > 0:
            signals.append({
                "signal_type": "soleops",
                "source": "soleops_inventory",
                "label": f"SoleOps: {inventory[0]} item(s) active, ${(inventory[1] or 0):,.0f} inventory value",
                "value": inventory[1] or 0,
                "content": json.dumps({"count": inventory[0], "value": inventory[1] or 0}),
                "weight": 0.8,
            })

        # Recent sales (last 7 days)
        recent_sales = execute(conn, f"""
            SELECT COUNT(*), SUM(sale_price) FROM soleops_inventory
            WHERE status = 'sold'
            AND sold_date >= date('now', '-7 days')
        """).fetchone()
        if recent_sales and recent_sales[0] and recent_sales[0] > 0:
            revenue = recent_sales[1] or 0
            signals.append({
                "signal_type": "soleops",
                "source": "soleops_inventory",
                "label": f"SoleOps: {recent_sales[0]} sale(s) this week — ${revenue:,.0f} revenue",
                "value": revenue,
                "content": json.dumps({"sales": recent_sales[0], "revenue": revenue}),
                "weight": 1.2,
            })

    except Exception:
        pass
    finally:
        conn.close()
    return signals


def _collect_all_signals(uid: int) -> list[dict]:
    """Collect behavioral signals from all sources in the app."""
    all_signals = []
    all_signals.extend(_collect_finance_signals(uid))
    all_signals.extend(_collect_todo_signals(uid))
    all_signals.extend(_collect_notes_signals(uid))
    all_signals.extend(_collect_creator_signals(uid))
    all_signals.extend(_collect_soleops_signals(uid))

    # Add manual context notes
    conn = get_conn()
    try:
        ctx_notes = execute(conn, f"""
            SELECT content, pinned FROM intent_context_notes
            WHERE user_id = {PH}
            ORDER BY pinned DESC, created_at DESC
            LIMIT 10
        """, (uid,)).fetchall()
        for row in ctx_notes:
            all_signals.append({
                "signal_type": "context",
                "source": "manual",
                "label": row[0][:80],
                "value": 1,
                "content": json.dumps({"note": row[0], "pinned": bool(row[1])}),
                "weight": 2.0 if row[1] else 1.0,
            })
    except Exception:
        pass
    finally:
        conn.close()

    return all_signals


